// Um bloco de código é um conjunto de instruções agrupadas entre chaves `{}`.

// Exemplo de código com blocos de código simples
{
    console.log("Passo 1");
    console.log("Passo 2");
    console.log("Passo 3");
}

// Exemplo de código com blocos de código com sub-blocos
{
    console.log("Passo 4");
    {
        console.log("Passo 5");
        console.log("Passo 6");
    }
}